  static SpokenLanguagePack packFor(String languageCode) {
    return _packs[languageCode] ?? _packs['en']!;
  }

  static String string(String languageCode, String key) {
    final requested = _packs[languageCode]?.strings[key];
    if (requested != null && requested.trim().isNotEmpty) {
      return requested;
    }
    final english = _packs['en']!.strings[key];
    if (english != null && english.trim().isNotEmpty) {
      return english;
    }
    return key;
  }

  static String spokenTime(DateTime dateTime, String languageCode) {
    final pack = packFor(languageCode);
    final hour = dateTime.hour % 12 == 0 ? 12 : dateTime.hour % 12;
    final minute = dateTime.minute.toString().padLeft(2, '0');
    final period =
        dateTime.hour < 12 ? string(pack.code, 'am') : string(pack.code, 'pm');
    return '$hour:$minute $period';
  }

  static String timeAnnouncement(String languageCode, DateTime dateTime) {
    return '${string(languageCode, "timePrefix")} ${spokenTime(dateTime, languageCode)}';
  }

  static String weatherDescription(String languageCode, int code) {
    return string(languageCode, 'weather_${_weatherBucket(code)}');
  }

  static String weatherAnnouncement(
    String languageCode, {
    required int weatherCode,
    required double temperatureC,
    bool cached = false,
  }) {
    final prefixKey = cached ? 'cachedWeatherPrefix' : 'weatherPrefix';
    final descriptor = weatherDescription(languageCode, weatherCode);
    final temp = temperatureC.toStringAsFixed(0);
    final unit = string(languageCode, 'degreeUnit');
    return '${string(languageCode, prefixKey)} $descriptor, $temp $unit.';
  }

  static String nextEventSnippet(
    String languageCode, {
    required String title,
    required int minutes,
  }) {
    if (minutes <= 0) {
      return '';
    }

    final label = string(languageCode, 'nextEventLabel');
    final inWord = string(languageCode, 'inWord');
    final minutesWord = string(languageCode, 'minutesWord');
    final hoursWord = string(languageCode, 'hoursWord');
    final andWord = string(languageCode, 'andWord');

    if (minutes < 60) {
      return '$label $title $inWord $minutes $minutesWord';
    }

    final hours = minutes ~/ 60;
    final remaining = minutes % 60;
    if (remaining == 0) {
      return '$label $title $inWord $hours $hoursWord';
    }
    return '$label $title $inWord $hours $hoursWord $andWord $remaining $minutesWord';
  }

  static String reminderAnnouncement(
    String languageCode, {
    required String category,
    required String title,
  }) {
    final prefix = string(languageCode, 'reminder_$category');
    return '$prefix. $title';
  }

  static String missedRemindersSummary(
    String languageCode, {
    required int count,
    required String preview,
    required bool hasMore,
  }) {
    final introKey = count == 1 ? 'missedSingular' : 'missedPlural';
    final intro =
        string(languageCode, introKey).replaceAll('{count}', '$count');
    final suffix = hasMore ? string(languageCode, 'andOthers') : '';
    return '$intro. $preview$suffix.';
  }

  static String stepProgressSummary(
    String languageCode, {
    required int stepsToday,
    required int goalSteps,
  }) {
    final prefix = string(languageCode, 'stepProgressPrefix');
    final goalLabel = string(languageCode, 'stepGoalLabel');
    final todayWord = string(languageCode, 'stepTodayWord');
    return '$prefix $stepsToday $todayWord. $goalLabel $goalSteps.';
  }

  static String stepMilestoneReached(
    String languageCode, {
    required int milestonePercent,
    required int stepsToday,
    required int goalSteps,
  }) {
    if (milestonePercent >= 100) {
      return '${string(languageCode, 'stepGoalReached')}. ${stepProgressSummary(languageCode, stepsToday: stepsToday, goalSteps: goalSteps)}';
    }
    final prefix = string(languageCode, 'stepMilestonePrefix');
    final goalLabel = string(languageCode, 'stepGoalLabel');
    return '$prefix. $milestonePercent percent of $goalLabel. ${stepProgressSummary(languageCode, stepsToday: stepsToday, goalSteps: goalSteps)}';
  }

  static String stepEndOfDaySummary(
    String languageCode, {
    required int stepsToday,
    required int goalSteps,
  }) {
    final prefix = string(languageCode, 'stepEndOfDayPrefix');
    return '$prefix. ${stepProgressSummary(languageCode, stepsToday: stepsToday, goalSteps: goalSteps)}';
  }

  static String _weatherBucket(int code) {
    if (code == 0) {
      return 'clear';
    }
    if (code <= 3) {
      return 'partlyCloudy';
    }
    if (code <= 48) {
      return 'foggy';
    }
    if (code <= 67) {
      return 'rainy';
    }
    if (code <= 77) {
      return 'snowy';
    }
    if (code <= 82) {
      return 'showers';
    }
    if (code <= 99) {
      return 'stormy';
    }
    return 'unknown';
  }
